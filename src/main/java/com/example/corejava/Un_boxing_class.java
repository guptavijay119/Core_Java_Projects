package com.example.corejava;

public class Un_boxing_class {

	public static void main(String[] args) {
		
		/* 2. Un-boxing

		Un-boxing means converting a Wrapper class object into a primitive data type automatically.

		Wrapper → Primitive
		Example of Un-boxing  */
		
		Integer a=100;
		
		int b=a;  //Un-boxing
		
		System.out.println(b);
		 
		
	

	}

}
